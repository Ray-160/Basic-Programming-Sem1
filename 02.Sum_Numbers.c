// Program to input and find sum of two numbers

#include <stdio.h>

int main(){
    int n1;
    int n2;
    int sum;
    
    printf("Enter First Number: ");
    scanf("%d",&n1);
    
    printf("Enter Second Number: ");
    scanf("%d",&n2);
    
    sum=n1+n2;
    
    printf("Sum: %d\t", sum);
    
    return 0;
 }
    
    
